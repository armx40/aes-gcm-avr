void RotWord(uint32_t *w)
{
    uint8_t tmp = (*w >> 24) & 0xff;
    *w = (uint32_t)((uint32_t)*w << 8);
    *w |= tmp;
}
void SubWord(uint32_t *w)
{
    //hex((unsigned char *)w,1,"WORD: ");
    //hex((unsigned char *)w,1,"SUBSTITUED WORD: ");
    uint32_t tmp_1 = (SBox[(uint8_t)(*w >> 24) & 0xff] << 24);
    uint32_t tmp_2 = (SBox[(uint8_t)(*w >> 16) & 0xff] << 16);
    uint32_t tmp_3 = (SBox[(uint8_t)(*w >> 8) & 0xff] << 8);
    uint32_t tmp_4 = (SBox[(uint8_t)(*w) & 0xff]);
    *w = tmp_1 | tmp_2 | tmp_3 | tmp_4;
}

void KeyExpansion(uint8_t *KEY, uint32_t *w, uint8_t Nk)
{
    uint32_t temp;
    uint8_t i = 0;
    while (i < Nk)
    {
        //printf("%dTH WORD\n", i);
        w[i] = (uint32_t)(KEY[4 * i] << 24) | (uint32_t)(KEY[(4 * i) + 1] << 16) | (uint32_t)(KEY[(4 * i) + 2] << 8) | (uint32_t)(KEY[(4 * i) + 3]);
        //printf("WORD: %08x\n\n", w[i]);
        i = i + 1;
    }
    i = Nk;
    while (i < (4 * (ROUND + 1)))
    {
        temp = w[i - 1];
        if (i % Nk == 0)
        {
            //printf("%dTH WORD\n", i);
            //printf("WORD: %08x\n", temp);
            RotWord(&temp);
            //printf("ROT WORD: %08x\n", temp);
            SubWord(&temp);
            //printf("SUB WORD: %08x\n", temp);
            temp = temp ^ Rcon[(i / Nk) - 1];
            //printf("FINAL TEMP: %08x\n", temp);
        }
        else if ((Nk > 6) && (i % Nk == 4))
        {
            //printf("%dTH WORD\n", i);
            //printf("WORD: %08x\n", temp);
            SubWord(&temp);
            //printf("SUB WORD: %08x\n", temp);
            //printf("FINAL TEMP: %08x\n", temp);
        }
        w[i] = w[i - Nk] ^ temp;
        //printf("W[%d]th WORD: %08x\n\n", i, W[i]);
        i = i + 1;
    }
}
